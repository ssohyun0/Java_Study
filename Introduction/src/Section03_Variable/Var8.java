package Section03_Variable;

public class Var8 {
    public static void main(String[] args) {
        //정수
        byte b = 127; //-127 ~ +127
        short s = 32767; // -32,768 ~ +32,767
        int i = 2147483647; // -2,147,483,648 ~ +2,147,483,647
        long l = 9223372036854775807L;

        //실수
        float f = 10.0f; //float는 뒤에 f를 붙여야 함
        double d = 10.0;
    }
}
